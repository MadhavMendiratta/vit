// Helper function to check if a room is important (labs, research facilities, etc.)
export function isImportantRoom(roomName: string): boolean {
  const lowerName = roomName.toLowerCase()

  // Check for important keywords
  return (
    lowerName.includes("lab") ||
    lowerName.includes("research") ||
    lowerName.includes("centre of excellence") ||
    lowerName.includes("center of excellence") ||
    lowerName.includes("facility") ||
    lowerName.includes("workshop") ||
    lowerName.includes("testing") ||
    lowerName.includes("processing") ||
    lowerName.includes("engineering") ||
    lowerName.includes("technology") ||
    lowerName.includes("spectroscopy") ||
    lowerName.includes("microscope") ||
    lowerName.includes("diffractometer") ||
    lowerName.includes("unit") ||
    lowerName.includes("chamber") ||
    lowerName.includes("shop") ||
    lowerName.includes("kitchen") ||
    lowerName.includes("bakery") ||
    lowerName.includes("confectionery") ||
    lowerName.includes("design") ||
    lowerName.includes("conference")
  )
}

// Get all lab names for search suggestions
export function getLabSuggestions(): string[] {
  const suggestions: string[] = []

  buildings.forEach((building) => {
    building.rooms.forEach((room) => {
      if (isImportantRoom(room.roomName)) {
        suggestions.push(room.roomName)
      }
    })
  })

  // Remove duplicates and sort
  return [...new Set(suggestions)].sort()
}

export function getBuildingData(id: string) {
  return buildings.find((building) => building.id === id)
}

